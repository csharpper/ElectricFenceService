        public static string GetNationFromMMSI(string mmsi)
        {
            int num = 0;
            if (int.TryParse(mmsi, out num))
                return GetNationFromMMSI(num);
            return "未知";
        }

        public static string GetNationFromMMSI(int mmsi)
        {
            if (mmsi > 100000000)
                return GetNationName(mmsi / 1000000);
            return "未知";
        }

        public static string GetNationName(int id)
        {
            lock (NationMgr.NationDicts)
            {
                if (NationDicts.ContainsKey(id))
                    return NationDicts[id];
                return "未知";
            }
        }
